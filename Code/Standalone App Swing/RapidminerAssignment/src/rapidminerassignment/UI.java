    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        try {
            // What to do with the file, e.g. display it in a TextArea
            //ta1.read( new FileReader( file.getAbsolutePath() ), null );

            HttpResponse<String> ret_data = null;

            try {
                ret_data = Unirest.post("http://localhost:8080/findMedian")
                        .header("Content-Type", "application/json")
                        .header("cache-control", "no-cache")
                        .body(data).asString();

                BufferedWriter writer = new BufferedWriter(new FileWriter(output_file));
                writer.write(ret_data.getBody().toString());
                jLabel2.setText(output_file.getAbsolutePath());
                jLabel2.setVisible(true);
                writer.close();

                //ta1.append(ret_data.getBody().toString());
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Please choose a file");
            }
        } catch (Exception ex) {
            System.out.println("problem accessing file" + file.getAbsolutePath());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

