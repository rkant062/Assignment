    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("CSV Files (*.csv)", "csv"));
        fileChooser.setAcceptAllFileFilterUsed(false);

        int returnVal = fileChooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file1;
            file1 = fileChooser.getSelectedFile();

            jLabel1.setText(file1.getAbsolutePath());
            jLabel1.setVisible(true);

            file = new File(jLabel1.getText());
            cj = new Csv2Json();

            try {
                data = cj.convertTojson(file);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
            }

            //jTable1=new JTable(data,data.getJSONArray(0));
            String column[] = new String[data.getJSONArray(0).length()];
            String datarow[][] = new String[data.length() - 1][data.getJSONArray(0).length()];
            for (int i = 0; i < data.getJSONArray(0).length(); i++) {
                column[i] = data.getJSONArray(0).get(i).toString();
            }
            for (int i = 0, k = 1; i < data.length() - 1; i++, k++) {
                for (int j = 0; j < data.getJSONArray(0).length(); j++) {
                    datarow[i][j] = data.getJSONArray(k).get(j).toString();
                    // System.out.print(""+ datarow[i][j]+" ");
                }
            }
            jTable2 = new JTable(datarow, column);
            jTable2.setBounds(30, 40, 200, 300);
            JScrollPane sp = new JScrollPane(jTable2);
            jf.add(sp);
            jf.setSize(300, 400);
           // jf.setVisible(true); 
            //JFrame.add(sp);
            // jComboBox1 = new JComboBox(column);
            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(column));
            jComboBox1.addActionListener(new java.awt.event.ActionListener() {
                public void actionPerformed(java.awt.event.ActionEvent evt) {
                    jComboBox1ActionPerformed(evt);
                }

                private void jComboBox1ActionPerformed(ActionEvent evt) {
                    throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
                }
            });

        } else {
            System.out.println("File access cancelled by user.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

